function search(query) {
  return new Promise((resolve, reject) => {
    if (!query) return resolve([]);

    const results = movies.filter((movie) =>
      movie.toUpperCase().includes(query.toUpperCase())
    );

    const fakeDelay = Math.random() * (300 - 100) + 100;
    setTimeout(() => resolve(results), fakeDelay);
  });
}

const inMemoryService = { search };
export default inMemoryService;
